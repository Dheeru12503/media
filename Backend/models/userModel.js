const mongoose = require("mongoose");

const userSchema = mongoose.Schema({
  profileImage: {
    type: String,
    required: true,
  },
  name: {
    type: String,
    required: true,
    trim: true,
  },
  email: {
    type: String,
    required: true,
    trim: true,
  },
  password: {
    type: String,
    required: true,
  },
});

module.exports = mongoose.model("userModel", userSchema);
